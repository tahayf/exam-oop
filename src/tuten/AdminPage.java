    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        e = Files.readArrayListFromFile("exams.ser");

        if(!txtN.getText().equals("") &&  !txtName.getText().equals("") && !txtA.getText().equals("") && !txtB.getText().equals("")){
                
            boolean flag = true;
            boolean flag2 = true;
            int N = Integer.parseInt(txtN.getText());
            int a = Integer.parseInt(txtA.getText());
            int b = Integer.parseInt(txtB.getText());
            
            if(a > b){
                flag = false;
                lblWarn.setText("'a' can not be greater than 'b'!");
            }
            
            //need to differ exam names otherwise program crashes
            for(Exam ex: e){
                if(ex.getExamName().equals(txtName.getText())){
                    flag2 = false;
                    lblWarn.setText("Exam name already exists!");
                }
            }         

            if(flag && flag2){
                e.add(new Exam(txtName.getText(),N,a,b));
                listModelE.addElement(txtName.getText() + " - " + txtN.getText() + " questions");
        
                Files.writeArrayListToFile(e, "exams.ser");
        
                txtName.setText("");
                txtN.setText("");
                txtA.setText("");
                txtB.setText("");
                lblWarn.setText("");
            }  
        }
        
        

    
       
    }//GEN-LAST:event_btnAddMouseClicked

