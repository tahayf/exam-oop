    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        
        Object selectedValue = listExams.getSelectedValue();
        
        if(selectedValue != null){
            e = Files.readArrayListFromFile("exams.ser");
            String splitted = listExams.getSelectedValue().split(" ")[0];
 
            Iterator<Exam> iterator = e.iterator();
            int i = 0,j = 0;
            while (iterator.hasNext()) {
                Exam ex = iterator.next();
                if (splitted.equals(ex.getExamName())) {
                    iterator.remove(); 
                    j = i;
                }
                i++;
            }
            listModelE.remove(j);
            Files.writeArrayListToFile(e, "exams.ser");
        }
        
       
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnChooseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChooseMouseClicked
        Object selectedValue = listDiaries.getSelectedValue();
        
        if(selectedValue != null){
            d = Files.readArrayListFromFile("diaries.ser");
            String date = listDiaries.getSelectedValue().split(" ")[4];
            String time = listDiaries.getSelectedValue().split(" ")[5];
            
            for(Diary di: d){ // we differ each diary via their date and time here
                if(di.getWhenStarted().format(formatter).split(" ")[0].equals(date) && di.getWhenStarted().format(formatter).split(" ")[1].equals(time)){
                    DiaryPage dp = new DiaryPage(di.getExam(),di.getWhoSolved(),di.getWhenStarted().format(formatter));
                    dp.setVisible(true);
                    
                }
            }
            
        }
    }//GEN-LAST:event_btnChooseMouseClicked

    private void btnChooseExamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnChooseExamMouseClicked
         Object selectedValue = listExams.getSelectedValue();
        
        if(selectedValue != null){
            e = Files.readArrayListFromFile("exams.ser");
            String examName = listExams.getSelectedValue().split(" ")[0];
            
            for(Exam ex: e){
                if(ex.getExamName().equals(examName)){
                    ScorePage sp = new ScorePage(ex.getScores());
                    sp.setVisible(true);
                }
            }
            
        }
        
    }//GEN-LAST:event_btnChooseExamMouseClicked

